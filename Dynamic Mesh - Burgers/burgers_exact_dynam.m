     function [x] = burgers_exact_dynam(z,t)
%...
%... this function computes an exact solution to Burgers' equation
%...
     global epsilon
%...
     r1=exp(0.05/epsilon*(-z+0.5-4.95*t));
     r2=exp(0.25/epsilon*(-z+0.5-0.75*t));
     r3=exp(0.5/epsilon*(-z+0.375));
%...     
     x=(0.1*r1+0.5*r2+r3)./(r1+r2+r3);